
'''
range():This function generate list of numbers in a sequence.
syntax:

     range(start,stop,step)

     start=> initialization
     stop => conditon
     step => increment/decrement [positive or negative]
     
'''

'''
x=list(range(1,15,1))
print(x)
'''

'''
x=list(range(2,20,2))
print(x)
'''

#No Step
'''
x=list(range(2,20))
print(x)
'''

#No step ans Start
'''
x=list(range(20)) #Default start is 0
print(x)
'''

#Error
'''
x=list(range())
print(x)

'''

x=list(range(15,5,-2))
print(x)

